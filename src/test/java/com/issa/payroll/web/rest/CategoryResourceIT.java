    @Test
    @Transactional
    void createCategory() throws Exception {
        int databaseSizeBeforeCreate = categoryRepository.findAll().size();
        // Create the Category
        restCategoryMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(category)))
            .andExpect(status().isCreated());

        // Validate the Category in the database
        List<Category> categoryList = categoryRepository.findAll();
        assertThat(categoryList).hasSize(databaseSizeBeforeCreate + 1);
        Category testCategory = categoryList.get(categoryList.size() - 1);
        assertThat(testCategory.getCode()).isEqualTo(DEFAULT_CODE);
        assertThat(testCategory.getLibAr()).isEqualTo(DEFAULT_LIB_AR);
        assertThat(testCategory.getLibEn()).isEqualTo(DEFAULT_LIB_EN);
        assertThat(testCategory.getUtil()).isEqualTo(DEFAULT_UTIL);
        assertThat(testCategory.getDateop()).isEqualTo(DEFAULT_DATEOP);
        assertThat(testCategory.getModifiedBy()).isEqualTo(DEFAULT_MODIFIED_BY);
        assertThat(testCategory.getOp()).isEqualTo(DEFAULT_OP);
        assertThat(testCategory.getIsDeleted()).isEqualTo(DEFAULT_IS_DELETED);
        assertThat(testCategory.getCreatedDate()).isEqualTo(DEFAULT_CREATED_DATE);
        assertThat(testCategory.getModifiedDate()).isEqualTo(DEFAULT_MODIFIED_DATE);
    }

